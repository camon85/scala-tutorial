object RelationalOperators {
  def main(args: Array[String]) {
    val a = 10
    val b = 20

    println("a == b = " + (a == b) )
    println("a != b = " + (a != b) )
    println("a > b = " + (a > b) )
    println("a < b = " + (a < b) )
    println("b >= a = " + (b >= a) )
    println("b <= a = " + (b <= a) )

    val one = new Integer(1)

    println(one == 1)

  }
}
